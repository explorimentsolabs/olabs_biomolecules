// Default language
export const defaultLanguage = (import.meta.env && import.meta.env.VITE_LANGUAGE) ? import.meta.env.VITE_LANGUAGE : 'en';

// Translation function
export function t(key, language = null) {
  const currentLang = language || defaultLanguage;
  const lang = translations[currentLang] || translations[defaultLanguage];
  
  if (!lang[key]) {
    console.warn(`Translation key "${key}" not found for language "${currentLang}"`);
    return key;
  }
  
  return lang[key];
}

// Translation function with interpolation
export function tFormat(key, params = {}, language = null) {
  let text = t(key, language);
  
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`{${param}}`, 'g'), params[param]);
  });
  
  return text;
}